    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        labdetailsdb lb1 = new labdetailsdb();

        lb1.setLabnum1(0);
        lb1.setRegid1(txtreg1.getText());
        lb1.setFaculty1(combof1.getSelectedItem().toString());
        lb1.setCourse1(Comboc1.getSelectedItem().toString());
        lb1.setSubcode1(txtsubcode1.getText());
        lb1.setLab_no1(combolab1.getSelectedItem().toString());
        

        boolean result = dbop.addpostgraduatelab(lb1);

        if(result){
            Dboperation2 db1 = new Dboperation2();
            String k1=db1.getLastId("postgraduate_lab","lab_num");
            String a1 = "Successfully Inserted!!" + "Your Lab registration number is "+k1;
            JOptionPane.showMessageDialog(this,a1);
            clearFields();
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "Oops error occured while inserting!!");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

