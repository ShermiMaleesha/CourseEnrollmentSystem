    public String[] fillCmb(int x){
        String ar[] = new String[10];
        try{
        Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm", "root", "");//get the connection
            String query = "SELECT * FROM postgraduate_subjects WHERE `Subject_code` = "+x;
            

         PreparedStatement   pst = (PreparedStatement) con.prepareStatement(query);
         
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
            ar[0] = rs.getString("lab_no_1");
            ar[1] = rs.getString("lab_no_2");
            ar[2] = rs.getString("lab_no_3");
            ar[3] = rs.getString("lab_no_4");
            }
            
            return ar;
        }
        catch(Exception ex){
        System.out.println("lab exception : "+ex);
            return null;
        }
        
         

    }
