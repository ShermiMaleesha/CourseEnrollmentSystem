    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try{
            pst = con.createStatement();
             
            String reg_no = txtreg_no1.getText();
            String faculty = combofac1.getSelectedItem().toString();
            String course=comboco1.getSelectedItem().toString();;         
            String year =comboy1.getSelectedItem().toString();
            String sem = combose1.getSelectedItem().toString();
            String sql = "SELECT Subject_code_1,Subject_code_2,Subject_code_3,Subject_code_4,Subject_code_5,Subject_code_6 FROM undergraduate_select_subject WHERE Reg_no= '"+reg_no+"' and Faculty ='"+faculty+"'and  Course ='"+course+"' and Year='"+year+"' and Semester='"+sem+"'";
            
            rs = pst.executeQuery(sql);
            
            if(rs.next()){
                txtsc13.setText(rs.getString("Subject_code_1"));
                txtsc14.setText(rs.getString("Subject_code_2"));
                txtsc15.setText(rs.getString("Subject_code_3"));
                txtsc1.setText(rs.getString("Subject_code_4"));
                txtsc2.setText(rs.getString("Subject_code_5"));
                txtsc3.setText(rs.getString("Subject_code_6"));
                                
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Subject not found");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

