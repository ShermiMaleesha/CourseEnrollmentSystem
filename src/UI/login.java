    private void butloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butloginActionPerformed
      
       try {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/nsbm","root","");
           String sql = "Select * from admin_login where username=? and password=? ";
           PreparedStatement pst = (PreparedStatement)con.prepareStatement(sql);
           pst.setString(1, txtusername.getText());
           pst.setString(2, txtpassword.getText());
           ResultSet rs = pst.executeQuery();
           
           if(rs.next()){
                MainFrame x = new MainFrame();
                x.setVisible(true);
           }else{
               JOptionPane.showMessageDialog(null, "Password is extermly wrong!\nClick OK & try again.","Wrong password",JOptionPane.ERROR_MESSAGE);
                txtusername.setText("");
                txtpassword.setText("");
                
           }
           
        
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
                
    }//GEN-LAST:event_butloginActionPerformed

