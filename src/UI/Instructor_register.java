    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        instructordetailsdb in1 = new instructordetailsdb();

        in1.setRegid(0);
        in1.setName(txtname.getText());
        in1.setNIC(txtNIC.getText());
        in1.setEmail(txtEmail.getText());
        in1.setPhoneNo(txtPhone_no.getText());
        in1.setFaculty(combofaculty.getSelectedItem().toString());
        in1.setSubject(txtSubjects.getText());
        in1.setLabno(txtlabno.getText());
        

        boolean result = db3.addinstructor(in1);

        if(result){
            Dboperation2 db1 = new Dboperation2();
            String k1=db1.getLastId("instructor","Reg_no");
            String a1 = "Successfully Inserted!!" + "Your registration number is "+k1;
            JOptionPane.showMessageDialog(this,a1);
            clearFields();
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "Oops error occured while inserting!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
